import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { FRONTIERS } from '../src/frontiers.ts';
import { INTERIORS } from '../src/interiors.ts';
import { INTERIORS_2 } from '../src/interiors-2.ts';

const destination = fileURLToPath(new URL('../src/atlas.ts', import.meta.url));
const check = process.argv.includes('--check');
const interiorSlugs = new Set([
  ...Object.keys(INTERIORS),
  ...Object.keys(INTERIORS_2),
]);

const entries = FRONTIERS.map((frontier) => ({
  id: frontier.id,
  atlasN: frontier.atlasN,
  slug: frontier.slug,
  title: frontier.title,
  qfocus: frontier.qfocus,
  domain: frontier.domain,
  cluster: frontier.cluster,
  scores: frontier.scores,
  citation: frontier.citation,
  brief: frontier.brief,
  stage: frontier.stage,
  members: frontier.members,
  activity: frontier.activity,
  ...(frontier.dormant === undefined ? {} : { dormant: frontier.dormant }),
  ...(frontier.outlier === undefined ? {} : { outlier: frontier.outlier }),
  ...(frontier.resolved === undefined ? {} : { resolved: frontier.resolved }),
  chart: frontier.chart,
  hasInterior: interiorSlugs.has(frontier.slug),
}));

const source = `/**
 * GENERATED by scripts/generate-atlas.mjs — do not edit by hand.
 *
 * This is the browser-safe L0 projection of FRONTIERS. Deep editorial content
 * and station interiors intentionally live behind the L1 data entry points.
 */
import type { FrontierEntry } from './frontiers';

export type FrontierAtlasEntry = Pick<FrontierEntry,
  | 'id'
  | 'atlasN'
  | 'slug'
  | 'title'
  | 'qfocus'
  | 'domain'
  | 'cluster'
  | 'scores'
  | 'citation'
  | 'brief'
  | 'stage'
  | 'members'
  | 'activity'
  | 'dormant'
  | 'outlier'
  | 'resolved'
  | 'chart'
> & { hasInterior: boolean };

export const FRONTIER_ATLAS: FrontierAtlasEntry[] = ${JSON.stringify(entries, null, 2)};

export const FRONTIER_ATLAS_BY_SLUG = new Map(
  FRONTIER_ATLAS.map((frontier) => [frontier.slug, frontier]),
);

export function frontierAtlasBySlug(slug: string): FrontierAtlasEntry | undefined {
  return FRONTIER_ATLAS_BY_SLUG.get(slug);
}
`;

if (check) {
  const current = await readFile(destination, 'utf8').catch(() => '');
  if (current !== source) {
    console.error('packages/data/src/atlas.ts is stale. Run pnpm --filter @frontier-isles/data generate:atlas.');
    process.exitCode = 1;
  }
} else {
  await writeFile(destination, source);
  console.log(`Generated ${entries.length} Atlas summaries at packages/data/src/atlas.ts.`);
}
